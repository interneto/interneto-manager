    <h2 id="classification-system">Classification system</h2>
    <p>13 main categories + Info:</p>
    <ul>
        <li>!nfo: Info of the Interneto project</li>
        <li>+Net: Internet related sites.</li>
        <li>Arts: Arts related web and platform sites.</li>
        <li>Business: Business and companies sites.</li>
        <li>Creators: Public content creators of platforms, like individuals, teams, projects, companies, etc.</li>
        <li>Content: Interesting audio, articles, images and video content of the internet.</li>
        <li>Education: Education sites.</li>
        <li>Ent: Entertainment sites.</li>
        <li>Health: Health sites.</li>
        <li>Media: Blogs, magazines and newspapers of all topics.</li>
        <li>Reference: Reference sites of the internet.</li>
        <li>Science: Science related websites.</li>
        <li>Society: Sites related to society.</li>
        <li>World: World and regional sites.
    </ul>

    <h2 id="explanation">Explanation</h2>
    <p>This project came from the idea of getting a free accessible categorized database of the internet sites. There are between 1,000 and 2,000 millions unique domains however less than 200 millions are active, around 80% of websites are inactive by parked domains or similar. Data from:</p>
    <ul>
        <li><a href="https://news.netcraft.com/archives/category/web-server-survey/">Web Server Survey - Netcraft News</a></li>
        <li><a href="https://majestic.com/reports/majestic-million">Majestic million</a>, first million domains with the most referring subnets.</li>
    </ul>
    <p>Categorize 200 million of domains that born and die is almost impossible but the main idea is to create a big list of functional and relevant webs. Two main projects have committed to do this work in the past: <a href="http://dmoz-odp.org">DMOZ (Directory of Mozilla)</a> and <a href="http://Curlie.org">Curlie (Collector of URLs)</a>, however DMOZ closed in 2017 and Curlie is very inactive.</p>
    <p>In addition, nowadays with the improvement of search engines the web directories have been abandoned and the searchers like Google, DuckDuckGo, Bing, Yandex are the trending.</p>
    <p>But web directories are still useful for various reasons:</p>
    <ul>
        <li>Does not consume energy of the servers.</li>
        <li>Does not use your privacy to target with ads and extract information.</li>
        <li>Have a big list of webs about the same topic.</li>
        <li>It is not sorted by marketing, ads or SEO position instead of search engines do. </li>
        <li>It is totally free as always you have a local database of links.</li>
        <li>You can download all the links and manage locally. </li>
    </ul>
    <p>The main issue about web directories is the way to search information, its hierarchical structure with categories and subcategories difficult the process of searching. Therefore, all efforts should be focus towards improving the quality of the directory structure. For that reason is necessary some tool that permit to categorize, organize and display content in a good way.</p>
    <p>The most popular search engines in terms of proportions. The spider represents the web crawler, the AI bot that index the Web.</p>
    <ul>
        <li>Google: 92.2% head of the spider controls the web</li>
        <li>Bing (also Yahoo!): 3.77% from Microsoft one the biggest corporation</li>
        <li>Baidu: 1.45% a big Chinese internet company</li>
        <li>Yandex: 0.68% a big Russian internet company</li>
        <li>At the bottom are represented the search engines that cares for your privacy, like DuckDuckGo (0.59%), Startpage, Qwant, PrivacyWall, YaCy (DAPP search engine). If you want to use the power of the spider you have to sell your soul.</li>
    </ul>
