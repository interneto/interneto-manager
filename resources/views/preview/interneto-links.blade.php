    <h2 class="pb-2 border-bottom">Interneto Links</h2>
    <p>
        Interneto links is web directory, a collection of categorized websites.
        The directory is structured in 13 main categories with subcategories inside where are accordingly stored the links.
        These links are homepage websites <a href="https://sub.domain.tld/">sub.domain.tld/</a>, but also there are two specific categories: "Content" for articles, audios, images, videos and "Creators" for content creators of platforms. Besides that, there are hashtags to improve the classification.</p>

